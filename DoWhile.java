package java_week3;

public class DoWhile {
    public static void main(String[] args) {
        int puncte = 15;
        do {
            System.out.println("am doar: " + puncte + " mai am de invatat!");
            puncte++;
        }
        while (puncte <= 17);
//        System.out.println("Felicitari pentru numarul: " + puncte + "obtinut!");
    }
}